import json
import subprocess
import time
from pathlib import Path

OAST_DIR = Path("/tmp/oast")
OAST_DIR.mkdir(exist_ok=True)

def start_interactsh():
    """Starts interactsh-client in the background and returns the payload URL."""
    ts = int(time.time() * 1000)
    out_json = OAST_DIR / f"interactsh_{ts}.json"
    
    # We grab the first output line using awk to reliably get the payload URL
    cmd = f"/home/work/go/bin/interactsh-client -json -o {out_json} | grep 'interact.sh' | head -n 1"
    
    proc = subprocess.Popen(
        f"/home/work/go/bin/interactsh-client -json -o {out_json}", 
        shell=True, 
        stdout=subprocess.PIPE, 
        stderr=subprocess.PIPE,
        text=True
    )
    
    # Wait for URL to appear in stdout
    payload_url = None
    for _ in range(10):
        line = proc.stdout.readline()
        if not line:
            break
        if "interact" in line or "oast" in line:
            import re
            m = re.search(r'([a-z0-9]+\.interact\.sh)', line)
            if m:
                payload_url = m.group(1)
                break
        time.sleep(0.5)
        
    if not payload_url:
        proc.kill()
        return "[error] Could not allocate OAST payload URL"
        
    return f"{payload_url}|{ts}|{proc.pid}"


def check_interactsh(ts: str):
    """Reads the JSON output generated by interactsh for a specific session ts."""
    out_json = OAST_DIR / f"interactsh_{ts}.json"
    if not out_json.exists():
        return "[no callbacks received yet]"
        
    content = out_json.read_text()
    if not content.strip():
        return "[no callbacks received yet]"
        
    results = []
    for line in content.strip().split('\n'):
        if not line.strip(): continue
        try:
            data = json.loads(line)
            p = data.get("protocol", "unknown")
            q = data.get("q-type", "")
            r = data.get("remote-address", "")
            results.append(f"[{p}] request from {r} (type: {q})")
            if "raw-request" in data:
                req_snipped = data["raw-request"][:500]
                results.append(f"Request data:\n{req_snipped}\n")
        except:
            pass
            
    if not results:
        return "[no callbacks received yet]"
        
    return "\n".join(results)
